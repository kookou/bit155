package day02;
/*switch 문 - 첫번째*/
public class Ex06 {
	public static void main(String[] args) {
		int number = 2;
		switch(number) {
		case 1:
			System.out.println("1입니다.");
		
		case 2:
			System.out.println("2입니다.");
		
		case 3:
			System.out.println("3입니다.");
			
		default:
			System.out.println("그외입니다.");

		}
	}

}
